'''
Task 1: Perform Basic Mathematical Operations
Problem Statement: Write a Python program that does the following:
    1.  Takes two numbers as input from the user.
    2.  Performs the basic mathematical operations on these two numbers:
            o	Addition
            o	Subtraction
            o	Multiplication
            o	Division
    3.  Displays the results of each operation on the screen. '''

#Solution

firstNumber = int(input('Enter the first number: ')) # Enter 1st number
secondNumber = int(input('Enter the second number: ')) # Enter 2nd number

print()  # blank space

print('Addition: ', firstNumber + secondNumber) # Addition
print('Substraction: ', firstNumber - secondNumber) # Subtraction
print('Multiplication: ', firstNumber * secondNumber) # Multiplication
print('Division: ', firstNumber / secondNumber) # Division


